  async uploadFile(file: File | { uri: string; name: string; type?: string; workspaceId?: string }, filename?: string): Promise<FileUploadResponse> {
    try {
      console.log('üì§ Starting file upload...');
      console.log('üîç File input type:', typeof file);
      console.log('üîç File input details:', JSON.stringify(file, null, 2));
      console.log('üîç Filename parameter:', filename);

      const formData = new FormData();

      // Add workspace ID if provided
      if (file && typeof file === 'object' && 'workspaceId' in file && file.workspaceId) {
        formData.append('workspaceId', file.workspaceId);
        console.log('üè¢ Adding workspace ID to upload:', file.workspaceId);
      }

      // Handle web File object vs mobile file object
      if (file instanceof File) {
        // Web File object
        console.log('üåê Detected web File object');
        formData.append('file', file);
        console.log('üåê Web file upload:', file.name, file.type, file.size, 'bytes');
      } else if (file && typeof file === 'object' && 'uri' in file) {
        // Mobile file object - React Native specific format
        console.log('üì± Detected mobile file object');
        console.log('üì± Original mobile file:', JSON.stringify(file, null, 2));

        // For React Native, we need to create a proper file object for FormData
        const mobileFile = {
          uri: file.uri,
          name: filename || file.name || 'unknown.pdf',
          type: file.type || 'application/pdf' // Default to PDF if no type
        };

        console.log('üì± Formatted mobile file for FormData:', JSON.stringify(mobileFile, null, 2));
        formData.append('file', mobileFile as any);
        console.log('üì± Mobile file appended to FormData successfully');
      } else if (typeof file === 'string') {
        // Handle case where file might be passed as URI string
        console.log('üì± Detected file as URI string:', file);
        const mobileFile = {
          uri: file,
          name: filename || 'unknown.pdf',
          type: 'application/pdf'
        };

        console.log('üì± Converted string URI to mobile file object:', JSON.stringify(mobileFile, null, 2));
        formData.append('file', mobileFile as any);
        console.log('üì± String URI file appended to FormData successfully');
      } else {
        console.error('‚ùå Invalid file format detected');
        console.error('‚ùå File type:', typeof file);
        console.error('‚ùå File instanceof File:', file instanceof File);
        console.error('‚ùå File has uri property:', file && typeof file === 'object' && 'uri' in file);
        console.error('‚ùå Full file object:', JSON.stringify(file, null, 2));
        throw new Error(`Invalid file format provided. Received: ${typeof file}, Expected: File object or mobile file object with uri property`);
      }

      console.log('üîÑ Sending upload request to:', API_ENDPOINTS.upload);
      console.log('üîÑ FormData prepared, making fetch request...');

      const response = await fetch(API_ENDPOINTS.upload, {
        method: 'POST',
        body: formData,
        // Don't set Content-Type header - let the browser set it with boundary
      });

      console.log('üì® Upload response received');
      console.log('üì® Response status:', response.status);
      console.log('üì® Response ok:', response.ok);
      console.log('üì® Response headers:', JSON.stringify([...response.headers.entries()], null, 2));

      if (!response.ok) {
        const errorText = await response.text();
        console.error('‚ùå Upload failed with status:', response.status);
        console.error('‚ùå Error response:', errorText);
        throw new Error(`Upload failed: ${response.status} - ${errorText}`);
      }

      const result: ApiResponse<{ file: FileUploadResponse }> = await response.json();
      const uploadedFile = result.file!;
      console.log('‚úÖ File uploaded successfully:', uploadedFile);

      // Auto-index PDF files for RAG
      if (uploadedFile.mimetype === 'application/pdf') {
        console.log('üîÑ Starting RAG indexing for PDF...');
        try {
          console.log(`üì¶ Importing RAG service...`);
          const { ragService } = await import('./ragService');
          console.log(`‚úÖ RAG service imported successfully`);

          console.log(`üîÑ Starting document indexing for file: ${uploadedFile.id}`);
          const indexResult = await ragService.indexDocument(uploadedFile.id);
          console.log('‚úÖ Document indexed for RAG successfully');
          console.log('üìä Indexing result:', JSON.stringify(indexResult, null, 2));
        } catch (ragError) {
          console.log('‚ö†Ô∏è RAG indexing failed (non-critical)');
          console.log('‚ùå RAG error details:', ragError);
          console.log('‚ùå RAG error type:', ragError.constructor.name);
          console.log('‚ùå RAG error message:', ragError.message);
          // Don't fail the upload if RAG indexing fails
        }
      } else {
        console.log(`üìÑ File is not PDF (${uploadedFile.mimetype}), skipping RAG indexing`);
      }

      return uploadedFile;
    } catch (error) {
      console.error('‚ùå File upload error occurred');
      console.error('‚ùå Error type:', error.constructor.name);
      console.error('‚ùå Error message:', error.message);
      console.error('‚ùå Error stack:', error.stack);
      throw error;
    }
  }

